/// Defines the various error types that can be generated by rltbl_db.
#[derive(Clone, Debug, PartialEq)]
#[non_exhaustive] // We may add more specific error types in the future.
pub enum DbError {
    /// An error that occurred while connecting to a database.
    ConnectError(String),
    /// An error in the arguments to a function that accessed the database.
    InputError(String),
    /// An error in the data retrieved from the database.
    DataError(String),
    /// An error that originated from the database.
    DatabaseError(String),
    /// An error with the data type of a value.
    DatatypeError(String),
    /// An error that occurred while attempting to parse a SQL string or value.
    ParseError(String),
    /// An error that occurred during serialization or deserialization.
    SerdeError(String),
}

impl std::error::Error for DbError {}

impl ser::Error for DbError {
    fn custom<T>(msg: T) -> Self
    where
        T: Display,
    {
        DbError::SerdeError(msg.to_string())
    }
}

impl de::Error for DbError {
    fn custom<T>(msg: T) -> Self
    where
        T: Display,
    {
        DbError::SerdeError(msg.to_string())
    }
}

impl std::fmt::Display for DbError {
    fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
        match self {
            DbError::ConnectError(err)
            | DbError::DataError(err)
            | DbError::InputError(err)
            | DbError::DatabaseError(err)
            | DbError::DatatypeError(err)
            | DbError::ParseError(err)
            | DbError::SerdeError(err) => write!(f, "{err}"),
        }
    }
}

#[async_trait]
pub trait DbQuery: Sync {
    /// Set the caching strategy.
    fn set_caching_strategy(&mut self, strategy: &CachingStrategy);

    /// Get the current caching strategy.
    fn get_caching_strategy(&self) -> CachingStrategy;

    /// When turned on, and the current [CachingStrategy] is not [None](CachingStrategy::None),
    /// SQL commands executed through the API will be automatically checked to see if they
    /// involve edits and/or drops of database tables. If they do then the cache will be
    /// automatically updated in accordance with the current [CachingStrategy].
    /// Note that setting this flag does not imply that the results of queries should be
    /// cached. Setting this flag only means that the current contents, if any, of the cache
    /// table should be kept up to date whenever the data in the database is edited
    /// via one of the query_* or execute() methods in [DbQuery]. To add new content to the cache
    /// that can be later be reused you must explicitly use the [DbQuery::cache()] method.
    /// To explicitly skip the housekeeping implied by setting the cache-aware-query flag, even
    /// when it is set to on, use [DbQuery::execute_no_cache_clean()] or
    /// [DbQuery::query_no_cache_clean()].
    fn set_cache_aware_query(&mut self, value: bool);

    /// Returns true if the cache-aware-query option is currently on.
    fn get_cache_aware_query(&self) -> bool;

    /// Execute the given SQL command with the given parameters, returning a vector of rows.
    /// If the result of the command exists in the query cache for the given tables, get the
    /// value from there instead of from the tables themselves, in accordance with the given
    /// [CachingStrategy].
    async fn cache(
        &self,
        sql: &str,
        params: impl IntoDbParams + Send + Copy + Sync,
    ) -> Result<DbRows, DbError> {
        match self.get_caching_strategy() {
            CachingStrategy::None => self.cache_tables(&[], sql, params).await,
            _ => {
                let tables_read = get_accessed_tables(sql)?;
                let tables_read: Vec<_> = tables_read.iter().map(|s| s.as_str()).collect();
                match tables_read.is_empty() {
                    false => self.cache_tables(&tables_read, sql, params).await,
                    true => Err(DbError::InputError(format!(
                        "No tables are read from in SQL: {sql}"
                    ))),
                }
            }
        }
    }

    /// Similar to [DbQuery::cache()]. This version accepts an explicit list of tables, which
    /// must correspond to the tables queried from in the given SQL command(s).
    async fn cache_tables(
        &self,
        tables: &[&str],
        sql: &str,
        params: impl IntoDbParams + Send + Copy + Sync,
    ) -> Result<DbRows, DbError> {
        let db_cache = async |tables: &[&str],
                              sql: &str,
                              params: &DbParams|
               -> Result<DbRows, DbError> {
            // Look in the cache to see if there is an entry corresponding to the given SQL
            // string for the given tables and parameters. If so, return the data from the
            // cache, otherwise execute the given SQL statement on the actualy specified
            // tables.
            let prefix = self.kind().param_prefix().to_string();
            let cache_sql = format!(
                r#"SELECT {prefix}1||rtrim(ltrim("value", '['), ']')||{prefix}2 AS "value"
                       FROM "{QUERY_CACHE_TABLE}"
                       WHERE "tables" = {prefix}3
                       AND "statement" = {prefix}4
                       AND "parameters" = {prefix}5
                       LIMIT 1"#,
            );
            let tables_param = format!(
                "[{}]",
                tables
                    .iter()
                    .map(|table| format!("\"{table}\""))
                    .collect::<Vec<_>>()
                    .join(", ")
            );
            let params_param = match params {
                DbParams::None => "[]".to_string(),
                DbParams::Positional(params) => {
                    let params = params.iter().map(|p| p.into()).collect::<Vec<String>>();
                    format!("[{}]", params.join(", "))
                }
            };
            let cache_params = &["[", "]", &tables_param, sql, &params_param];

            let strings = {
                let rows = self.query_no_cache_clean(&cache_sql, cache_params).await?;
                let strings = rows
                    .iter()
                    .map(|row| match row.values().nth(0) {
                        Some(value) => Ok(value.into()),
                        None => Err(DbError::DataError("Empty row".to_owned())),
                    })
                    .collect::<Vec<_>>();
                let strings: Result<Vec<String>, DbError> = strings.into_iter().collect();
                strings?
            };
            match strings.first() {
                Some(values) => {
                    let db_rows: Vec<DbRow> = serde_json::from_str(values).map_err(|err| {
                        DbError::DataError(format!("Error serializing values '{values}': {err}"))
                    })?;
                    // Only views need to be verified every time they are accessed. Tables
                    // do not because they do not have any dependencies.
                    if which_are_views(&self.pool(), tables).await?.len() > 0 {
                        update_last_verified(&self.pool(), tables, sql, &params).await?;
                    }
                    Ok(DbRows { content: db_rows })
                }
                None => {
                    let db_rows = self.query_no_cache_clean(sql, params).await?;
                    let rows_as_string = {
                        let mut rows_as_string = vec![];
                        for db_row in db_rows.iter() {
                            let db_row = serde_json::to_string(db_row).map_err(|err| {
                                DbError::DataError(format!("Invalid data ({err}): {db_row:?}"))
                            })?;
                            rows_as_string.push(db_row);
                        }
                        format!("[{}]", rows_as_string.join(", "))
                    };
                    let insert_sql = format!(
                        r#"INSERT INTO "{QUERY_CACHE_TABLE}"
                           ("tables", "statement", "parameters", "value")
                           VALUES ({prefix}1, {prefix}2, {prefix}3, {prefix}4)"#,
                        prefix = self.kind().param_prefix(),
                    );
                    let insert_params = [&tables_param, sql, &params_param, &rows_as_string];
                    self.execute_no_cache_clean(&insert_sql, &insert_params)
                        .await?;
                    Ok(db_rows)
                }
            }
        };

        let mem_cache = async |tables: &[&str],
                               sql: &str,
                               params: &DbParams,
                               cache_size: usize|
               -> Result<DbRows, DbError> {
            let params = &params.into_db_params();
            let mem_key = MemoryQueryCacheKey {
                tables: format!(
                    "[{}]",
                    tables
                        .iter()
                        .map(|table| format!("\"{table}\""))
                        .collect::<Vec<_>>()
                        .join(", ")
                ),
                statement: sql.to_string(),
                parameters: format!("{params:?}"),
            };
            let cached_rows = {
                let cache = get_memory_query_cache()?;
                match cache.get(&mem_key) {
                    Some(mem_value) => Some(mem_value.content.to_vec()),
                    None => None,
                }
            };
            match cached_rows {
                Some(db_rows) => {
                    // Only views need to be verified every time they are accessed. Tables
                    // do not because they do not have any dependencies.
                    if which_are_views(&self.pool(), tables).await?.len() > 0 {
                        update_last_verified(&self.pool(), tables, sql, &params).await?;
                    }
                    Ok(DbRows { content: db_rows })
                }
                None => {
                    let db_rows = self.query_no_cache_clean(sql, params).await?;
                    let mut cache = get_memory_query_cache()?;
                    // If the number of entries exceeds the allowed cache size, remove the oldest
                    // keys first.
                    // TODO: We may want to do something smarter here. E.g., we could record the
                    // length of time a query takes and/or the number of times it was requested
                    // in order to determine which entries to delete (and/or the size of the
                    // result).
                    while cache.len() > cache_size {
                        cache.shift_remove_index(0);
                    }
                    cache.insert(
                        mem_key,
                        MemoryQueryCacheValue {
                            content: db_rows.to_vec(),
                            last_verified: SystemTime::now()
                                .duration_since(UNIX_EPOCH)
                                .map_err(|err| {
                                    DbError::DataError(format!("Error getting epoch time: {err}"))
                                })?
                                .as_millis(),
                        },
                    );
                    Ok(db_rows)
                }
            }
        };

        match self.get_caching_strategy() {
            CachingStrategy::None => {
                let rows = self.query_no_cache_clean(sql, params).await?;
                Ok(rows)
            }
            CachingStrategy::TruncateAll | CachingStrategy::Truncate => {
                ensure_cache_tables_exist(&self.pool()).await?;
                update_cached_views(&self.pool(), tables).await?;
                let rows = db_cache(tables, sql, &params.into_db_params()).await?;
                Ok(rows)
            }
            CachingStrategy::Trigger => {
                let views = which_are_views(&self.pool(), tables)
                    .await?
                    .into_iter()
                    .collect::<HashSet<_>>();
                for table in tables
                    .into_iter()
                    .cloned()
                    .collect::<HashSet<_>>()
                    .difference(&views.iter().map(|v| v.as_str()).collect::<HashSet<_>>())
                    .collect::<Vec<_>>()
                {
                    ensure_caching_triggers_exist_for_table(&self.pool(), &table).await?;
                }
                for view in &views {
                    ensure_caching_triggers_exist_for_view(&self.pool(), view).await?;
                }
                let rows = db_cache(&tables, sql, &params.into_db_params()).await?;
                Ok(rows)
            }
            CachingStrategy::Memory(cache_size) => {
                update_cached_views(&self.pool(), tables).await?;
                let rows = mem_cache(tables, sql, &params.into_db_params(), cache_size).await?;
                Ok(rows)
            }
        }
    }

    /// Get the kind of SQL database. See [DbKind] for the supported database kinds.
    fn kind(&self) -> DbKind;

    /// Get the underlying database pool
    fn pool(&self) -> AnyPool;

    /// Given a table, return a [ColumnMap] from column names to column SQL types.
    async fn columns(&self, table: &str) -> Result<ColumnMap, DbError> {
        let mut columns = ColumnMap::new();
        let (sql, params) = self.kind().columns_sql(table);
        let rows = self.query_no_cache_clean(&sql, params).await?;
        for row in rows.iter() {
            match (
                row.get("column_name")
                    .and_then(|value| Some::<String>(value.into())),
                row.get("data_type")
                    .and_then(|value| Some::<String>(value.into())),
            ) {
                (Some(column), Some(data_type)) => {
                    columns.insert(column.to_string(), data_type.to_lowercase().to_string())
                }
                _ => {
                    return Err(DbError::DataError(format!(
                        "Error getting columns for table '{table}'"
                    )));
                }
            };
        }

        match columns.is_empty() {
            true => Err(DbError::DataError(format!(
                "No information found for table '{table}'"
            ))),
            false => Ok(columns),
        }
    }

    /// Retrieve the primary key column names for a given table.
    async fn primary_keys(&self, table: &str) -> Result<Vec<String>, DbError> {
        let (sql, params) = self.kind().primary_keys_sql(table);
        let rows = self.query_no_cache_clean(&sql, params).await?;
        rows.iter()
            .map(|row| {
                match row
                    .get("column_name")
                    .and_then(|name| Some::<String>(name.into()))
                {
                    Some(pk_col) => Ok(pk_col.to_string()),
                    None => Err(DbError::DataError("Empty row".to_owned())),
                }
            })
            .collect()
    }

    /// Execute the given SQL command with the given parameters, returning nothing.
    async fn execute(&self, sql: &str, params: impl IntoDbParams + Send) -> Result<(), DbError> {
        let params = params.into_db_params();
        match params {
            DbParams::None => self.query(sql, ()).await?,
            _ => self.query(sql, params).await?,
        };
        Ok(())
    }

    /// Execute the given SQL command with the given parameters, returning nothing, and without
    /// updating the cache, regardless of whether the cache-aware-query option
    /// (see [DbQuery::set_cache_aware_query()]) has been set.
    async fn execute_no_cache_clean(
        &self,
        sql: &str,
        params: impl IntoDbParams + Send,
    ) -> Result<(), DbError> {
        let params = params.into_db_params();
        match params {
            DbParams::None => self.query_no_cache_clean(sql, ()).await?,
            _ => self.query_no_cache_clean(sql, params).await?,
        };
        Ok(())
    }

    /// Sequentially execute a semicolon-delimited list of statements, without parameters.
    fn execute_batch(&self, sql: &str) -> impl Future<Output = Result<(), DbError>> + Send;

    /// Execute the given SQL command, with the given parameters, returning a vector of rows.
    fn query(
        &self,
        sql: &str,
        params: impl IntoDbParams + Send,
    ) -> impl Future<Output = Result<DbRows, DbError>> + Send;

    /// Execute the given SQL command using the given parameters, returning a vector of rows,
    /// without updating the cache, regardless of whether the cache-aware-query option
    /// (see [DbQuery::set_cache_aware_query()]) has been set.
    fn query_no_cache_clean(
        &self,
        sql: &str,
        params: impl IntoDbParams + Send,
    ) -> impl Future<Output = Result<DbRows, DbError>> + Send;

    /// Insert rows into the given columns of the given table. If an input row does not have a
    /// key corresponding to one of the given columns, use NULL as the value of that column when
    /// inserting the row to the table.
    fn insert(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::insert()], but in addition this function also returns the data that was
    /// inserted into the columns included in `returning`, or all of the inserted data if
    /// `returning` is an empty list.
    fn insert_returning(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<DbRows, DbError>>;

    /// Update the given columns of the given table using the given rows. The table should have a
    /// primary key and any columns that are part of the primary key should be present within each
    /// input row. The primary key column values will be used as a way of identifying the rows to
    /// update, while the other columns in the row will be updated to the given new values.
    fn update(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::update()], but in addition this function also returns the data that was
    /// updated for the columns included in `returning`, or all of the updated data if
    /// `returning` is an empty list.
    fn update_returning(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<DbRows, DbError>>;

    /// Attempt to insert the given rows to the given table, similarly to [DbQuery::insert()].
    /// In case there is a conflict, update the table instead, similarly to [DbQuery::update()].
    fn upsert(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::upsert()], but in addition this function also returns the data that was
    /// upserted for the columns included in `returning`, or all of the upserted data if
    /// `returning` is an empty list.
    fn upsert_returning(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<DbRows, DbError>>;

    /// Check whether the given table exists in the database.
    async fn table_exists(&self, table: &str) -> Result<bool, DbError> {
        Ok(which_are_tables(&self.pool(), &[table]).await?.len() == 1)
    }

    /// Check whether the given view exists in the database.
    async fn view_exists(&self, view: &str) -> Result<bool, DbError> {
        Ok(which_are_views(&self.pool(), &[view]).await?.len() == 1)
    }

    /// Drop the given table from the database. Note that for PostgreSQL (see
    /// <https://www.postgresql.org/docs/current/sql-droptable.html>), if the dropped table,
    /// say table1, appears in a foreign key constraint for another table, say table2, then
    /// table2's foreign constraint will be removed, but table2 will not be dropped.
    fn drop_table(&self, table: &str) -> impl Future<Output = Result<(), DbError>> + Send;

    /// Drop the given view from the database.
    fn drop_view(&self, view: &str) -> impl Future<Output = Result<(), DbError>> + Send;
}

/// Get the SQL code that is used to define the given view.
pub async fn get_view_sql(pool: &impl DbQuery, view: &str) -> Result<String, DbError> {
    let view_sql = {
        let rows = {
            let (sql, params) = pool.kind().view_sql_sql(view);
            pool.query_no_cache_clean(&sql, &params).await?
        };
        match rows.first() {
            Some(row) => row
                .get("sql")
                .ok_or(DbError::DataError("No column 'sql' in row".to_string()))?
                .to_string(),
            None => {
                return Err(DbError::DataError(format!(
                    "No view definition found for '{view}'"
                )));
            }
        }
    };
    Ok(view_sql)
}

/// Return a list of the objects from the given list that are tables.
pub async fn which_are_tables(
    pool: &impl DbQuery,
    objects: &[&str],
) -> Result<Vec<String>, DbError> {
    let mut tables = vec![];
    let mut unknowns = vec![];
    // Start by looking for the given objects in the meta cache:
    for object in objects {
        if exists_in_meta_cache(&format!("{object}_TABLE"))? {
            tables.push(object.to_string());
        } else if !exists_in_meta_cache(&format!("{object}_VIEW"))? {
            unknowns.push(object.to_string());
        }
    }

    // Query the database for the status of any unknowns:
    if !unknowns.is_empty() {
        let (sql, params) = pool
            .kind()
            .which_are_tables_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
        let rows = pool.query_no_cache_clean(&sql, params).await?;
        for row in rows.iter() {
            let table = row
                .get("table_name")
                .ok_or(DbError::DataError("No table_name found in row".to_string()))?
                .to_string();
            let mut cache = get_meta_cache()?;
            cache.insert(format!("{table}_TABLE"));
            tables.push(table);
        }

        let (sql, params) = pool
            .kind()
            .which_are_views_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
        let rows = pool.query_no_cache_clean(&sql, params).await?;
        for row in rows.iter() {
            let view = row
                .get("view_name")
                .ok_or(DbError::DataError("No view_name found in row".to_string()))?
                .to_string();
            let mut cache = get_meta_cache()?;
            cache.insert(format!("{view}_VIEW"));
        }
    }
    Ok(tables)
}

/// Return a list of the objects from the given list that are views.
pub async fn which_are_views(
    pool: &impl DbQuery,
    objects: &[&str],
) -> Result<Vec<String>, DbError> {
    let mut views = vec![];
    let mut unknowns = vec![];
    // Start by looking for the given objects in the meta cache:
    for object in objects {
        if exists_in_meta_cache(&format!("{object}_VIEW"))? {
            views.push(object.to_string());
        } else if !exists_in_meta_cache(&format!("{object}_TABLE"))? {
            unknowns.push(object.to_string());
        }
    }

    // Query the database for the status of any unknowns:
    if !unknowns.is_empty() {
        let (sql, params) = pool
            .kind()
            .which_are_views_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
        let rows = pool.query_no_cache_clean(&sql, params).await?;
        for row in rows.iter() {
            let view = row
                .get("view_name")
                .ok_or(DbError::DataError("No view_name found in row".to_string()))?
                .to_string();
            let mut cache = get_meta_cache()?;
            cache.insert(format!("{view}_VIEW"));
            views.push(view);
        }

        let (sql, params) = pool
            .kind()
            .which_are_tables_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
        let rows = pool.query_no_cache_clean(&sql, params).await?;
        for row in rows.iter() {
            let table = row
                .get("table_name")
                .ok_or(DbError::DataError("No table_name found in row".to_string()))?
                .to_string();
            let mut cache = get_meta_cache()?;
            cache.insert(format!("{table}_TABLE"));
        }
    }
    Ok(views)
}
