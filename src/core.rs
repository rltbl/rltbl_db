/// Strategy to use when caching query results
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum CachingStrategy {
    /// No Caching.
    None,
    /// Truncate the entire cache when it is dirty.
    TruncateAll,
    /// Truncate entries only for edited tables when the cache is dirty.
    Truncate,
    /// Truncate cache entries, for edited tables only, automatically whenever tables are edited.
    Trigger,
    /// Similar to Truncate, but use an in-memory cache.
    Memory(usize),
}

/// The structure used to look up query results in the in-memory cache.
#[derive(Clone, Debug, Eq, PartialEq, Hash)]
pub struct MemoryQueryCacheKey {
    pub tables: String,
    pub statement: String,
    pub parameters: String,
}

/// Represents the value of an entry in the in-memory cache.
#[derive(Clone, Debug)]
pub struct MemoryQueryCacheValue {
    pub content: Vec<JsonRow>,
    pub last_verified: u128,
}

impl FromStr for CachingStrategy {
    type Err = DbError;

    fn from_str(strategy: &str) -> Result<Self, DbError> {
        match strategy.to_lowercase().as_str() {
            "none" => Ok(CachingStrategy::None),
            "truncate_all" => Ok(CachingStrategy::TruncateAll),
            "truncate" => Ok(CachingStrategy::Truncate),
            "trigger" => Ok(CachingStrategy::Trigger),
            strategy if strategy.starts_with("memory") => {
                let elems = strategy.split(":").collect::<Vec<_>>();
                let cache_size = {
                    if elems.len() < 2 {
                        DEFAULT_MEMORY_CACHE_SIZE
                    } else {
                        let cache_size = elems[1];
                        match cache_size.parse::<usize>() {
                            Ok(0) => DEFAULT_MEMORY_CACHE_SIZE,
                            Ok(size) => size,
                            Err(err) => return Err(DbError::InputError(format!(
                                "Error parsing memory cache size specification: '{cache_size}': \
                                 {err}"
                            ))
                            .into()),
                        }
                    }
                };
                Ok(CachingStrategy::Memory(cache_size))
            }
            _ => {
                return Err(
                    DbError::InputError(format!("Unrecognized strategy: {strategy}")).into(),
                );
            }
        }
    }
}

impl Display for CachingStrategy {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            CachingStrategy::None => write!(f, "none"),
            CachingStrategy::TruncateAll => write!(f, "truncate_all"),
            CachingStrategy::Truncate => write!(f, "truncate"),
            CachingStrategy::Trigger => write!(f, "trigger"),
            CachingStrategy::Memory(size) => write!(f, "memory:{size}"),
        }
    }
}

#[async_trait]
pub trait DbQuery {
    /// Get the kind of SQL database: SQLite or PostgreSQL.
    fn kind(&self) -> DbKind;

    /// Set the caching strategy.
    fn set_caching_strategy(&mut self, strategy: &CachingStrategy);

    /// Get the current caching strategy.
    fn get_caching_strategy(&self) -> CachingStrategy;

    /// When turned on, and the current [CachingStrategy] is not [None](CachingStrategy::None),
    /// SQL commands executed through the API will be automatically checked to see if they
    /// involve edits and/or drops of database tables. If they do then the cache will be
    /// automatically updated in accordance with the current [CachingStrategy].
    fn set_cache_aware_query(&mut self, value: bool);

    /// Returns true if the cache_aware_query option is currently on.
    fn get_cache_aware_query(&self) -> bool;

    /// Ensure that the query cache table and the table cache table exist.
    async fn ensure_cache_tables_exist(&self) -> Result<(), DbError> {
        if !exists_in_meta_cache(QUERY_CACHE_TABLE)? || !exists_in_meta_cache(TABLE_CACHE_TABLE)? {
            for special_table in [QUERY_CACHE_TABLE, TABLE_CACHE_TABLE] {
                let sql = match special_table {
                    table if table == QUERY_CACHE_TABLE => {
                        self.kind().create_query_cache_table_sql()
                    }
                    table if table == TABLE_CACHE_TABLE => {
                        self.kind().create_table_cache_table_sql()
                    }
                    _ => unreachable!(),
                };
                match self.execute_no_cache(&sql, ()).await {
                    Ok(_) => (),
                    Err(_) => {
                        // Since we are not using transactions, a race condition could occur in
                        // which two or more threads are trying to create the cache at the same
                        // time, triggering a primary key violation in the metadata table. So if
                        // there is an error creating the cache table we just check that it exists
                        // and if it does we assume that all is ok.
                        match self.table_exists(special_table).await? {
                            false => {
                                return Err(DbError::DatabaseError(format!(
                                    "The cache table '{special_table}' could not be created"
                                )));
                            }
                            true => (),
                        }
                    }
                };
                let mut cache = get_meta_cache()?;
                cache.insert(special_table.to_string());
            }
        }
        Ok(())
    }

    /// Ensure that caching triggers exist for the given table. Note that this function calls
    /// [DbQuery::ensure_cache_tables_exist()] implicitly.
    async fn ensure_caching_triggers_exist_for_table(&self, table: &str) -> Result<(), DbError> {
        let table_triggers_name = format!("{table}_triggers");
        if !exists_in_meta_cache(&table_triggers_name)? {
            self.ensure_cache_tables_exist().await?;
            let sql = self
                .kind()
                .create_table_caching_triggers_for_table_sql(&table)?
                .join(";\n");
            self.execute_batch(&sql).await?;

            // Indicate that triggers exist for `table`:
            let mut cache = get_meta_cache()?;
            cache.insert(table_triggers_name);
        }

        Ok(())
    }

    /// Ensure that caching triggers exist for the source tables of the given view. Note that
    /// this function calls [DbQuery::ensure_cache_tables_exist()] implicitly.
    async fn ensure_caching_triggers_exist_for_view(&self, view: &str) -> Result<(), DbError> {
        let view_triggers_name = format!("{view}_triggers");
        if !exists_in_meta_cache(&view_triggers_name)? {
            self.ensure_cache_tables_exist().await?;
            let view_sql = self.get_view_sql(&view).await?;
            let source_tables = get_view_tables(&view_sql)?;
            for source_table in source_tables.iter() {
                // Add a trigger to clean entries from the cache for the source table itself:
                let sql = self
                    .kind()
                    .create_table_caching_triggers_for_table_sql(&source_table)?
                    .join(";\n");
                self.execute_batch(&sql).await?;
                // Add a trigger to clean entries from the cache for the view:
                let sql = self
                    .kind()
                    .create_table_caching_triggers_for_view_sql(&source_table, &view)?
                    .join(";\n");
                self.execute_batch(&sql).await?;
                // Add an entry for the source table triggers to the metacache. If there is another
                // entry for this source table it will be overwritten, which is desirable in
                // case it was not previously known if the table was the source table for a view.
                let mut cache = get_meta_cache()?;
                let source_triggers_name = format!("{source_table}_triggers");
                cache.insert(source_triggers_name);
            }
        }
        Ok(())
    }

    /// Parse the given semi-colon-separated SQL commands and determine which tables will be
    /// affected (either edited or dropped) by the commands, then ensure that there are no
    /// entries for those tables in the cache in accordance with the current [CachingStrategy].
    async fn clear_cache_for_affected_tables(&self, sql: &str) -> Result<(), DbError> {
        if self.get_caching_strategy() != CachingStrategy::None {
            let (edited_tables, dropped_tables): (Vec<_>, Vec<_>) = {
                let (edited_tables, dropped_tables) = get_affected_tables(sql)?;
                (
                    edited_tables.into_iter().collect(),
                    dropped_tables.into_iter().collect(),
                )
            };
            if !edited_tables.is_empty() {
                let edited_tables: Vec<_> = edited_tables.iter().map(|t| t.as_str()).collect();
                self.clear_cache_for_edited_tables(&edited_tables).await?;
            }
            if !dropped_tables.is_empty() {
                let dropped_tables: Vec<_> = dropped_tables.iter().map(|t| t.as_str()).collect();
                self.clear_cache_for_dropped_tables(&dropped_tables).await?;
            }
        }
        Ok(())
    }

    // Triggers cannot apply to DROP commands, only to INSERT, UPDATE, DELETE, or TRUNCATE.
    // See https://www.postgresql.org/docs/current/sql-createtrigger.html and
    // https://sqlite.org/lang_createtrigger.html. Note that PostgreSQL has the concept
    // of an "event trigger":
    // https://www.pgtutorial.com/postgresql-tutorial/postgresql-event-triggers/ which could
    // be used, but SQLite has no such capability. To workaround the limitation in SQLite, we
    // define two clear_cache_() functions, one for edited tables, and one for dropped tables.
    // In the case of a dropped table, unlike an edit, we cannot rely on the caching trigger,
    // when we are using the [CachingStategy::Trigger] strategy, to automatically delete the
    // entries from the cache for those tables, since those triggers will have beeen dropped
    // along with the table.
    // Although strictly speaking, PostgreSQL (which has event triggers) is not subject to this
    // limitation, for simplicity we will not be creating a PostgreSQL event trigger and we will
    // use both functions below for both database types.

    /// Clear the entries from the cache table for the given list of tables, using the
    /// current [CachingStrategy], under the assumption that the tables in the given list have
    /// all just been edited (i.e., truncated, deleted from, inserted to, or updated).
    async fn clear_cache_for_edited_tables(&self, tables: &[&str]) -> Result<(), DbError> {
        match self.get_caching_strategy() {
            CachingStrategy::None | CachingStrategy::Trigger => (),
            CachingStrategy::TruncateAll => {
                self.update_last_modified_times(tables).await?;
                self.delete_query_cache_entries(&[]).await?
            }
            CachingStrategy::Truncate => {
                self.update_last_modified_times(tables).await?;
                self.delete_query_cache_entries(tables).await?
            }
            CachingStrategy::Memory(_) => {
                self.update_last_modified_times(tables).await?;
                clear_memory_query_cache(tables)?;
            }
        };
        Ok(())
    }

    /// Clear the entries from the cache table for the given list of tables, using the
    /// current [CachingStrategy], under the assumption that the tables in the given list have
    /// all just been dropped.
    async fn clear_cache_for_dropped_tables(&self, tables: &[&str]) -> Result<(), DbError> {
        // Do not do anything if the dropped tables include the cache tables themselves:
        if tables
            .iter()
            .any(|table| [QUERY_CACHE_TABLE, TABLE_CACHE_TABLE].contains(table))
        {
            return Ok(());
        }

        match self.get_caching_strategy() {
            CachingStrategy::None => (),
            CachingStrategy::TruncateAll => {
                self.update_last_modified_times(tables).await?;
                self.delete_query_cache_entries(&[]).await?
            }
            CachingStrategy::Trigger | CachingStrategy::Truncate => {
                self.update_last_modified_times(tables).await?;
                self.delete_query_cache_entries(tables).await?
            }
            CachingStrategy::Memory(_) => {
                self.update_last_modified_times(tables).await?;
                clear_memory_query_cache(&tables)?
            }
        };

        // Indicate that any triggers for these tables no longer exist.
        let mut meta_cache = get_meta_cache()?;
        for table in tables {
            if *table == QUERY_CACHE_TABLE {
                meta_cache.remove(QUERY_CACHE_TABLE);
            } else if *table == TABLE_CACHE_TABLE {
                meta_cache.remove(TABLE_CACHE_TABLE);
            } else {
                meta_cache.remove(&format!("{table}_triggers"));
                meta_cache.remove(&format!("{table}_VIEW"));
                meta_cache.remove(&format!("{table}_TABLE"));
            }
        }
        Ok(())
    }

    /// Update the last verified time of the query cache entry identified by the triple:
    /// (tables, statement, params).
    async fn update_last_verified(
        &self,
        tables: &[&str],
        statement: &str,
        params: &Params,
    ) -> Result<(), DbError> {
        match self.get_caching_strategy() {
            CachingStrategy::Memory(_) => {
                let mut cache = get_memory_query_cache()?;
                let epoch_now = SystemTime::now()
                    .duration_since(UNIX_EPOCH)
                    .map_err(|err| {
                        DbError::DataError(format!("Error getting epoch time: {err}"))
                    })?;
                let mem_key = MemoryQueryCacheKey {
                    tables: format!(
                        "[{}]",
                        tables
                            .iter()
                            .map(|table| format!("\"{table}\""))
                            .collect::<Vec<_>>()
                            .join(", ")
                    ),
                    statement: statement.to_string(),
                    parameters: format!("{params:?}"),
                };
                match cache.get_mut(&mem_key) {
                    Some(value) => value.last_verified = epoch_now.as_millis(),
                    None => (),
                };
            }
            _ => match self.table_exists(QUERY_CACHE_TABLE).await? {
                true => {
                    let tables_param = format!(
                        "[{}]",
                        tables
                            .iter()
                            .map(|table| format!("\"{table}\""))
                            .collect::<Vec<_>>()
                            .join(", ")
                    );
                    self.execute(
                        &format!(
                            r#"UPDATE "{QUERY_CACHE_TABLE}"
                               SET "last_verified" = {ts}
                               WHERE "tables" = {p}1
                               AND "statement" = {p}2
                               AND "parameters" = {p}3"#,
                            p = self.kind().param_prefix(),
                            ts = self.kind().get_epoch_time_sql(),
                        ),
                        &[
                            &format!("[{tables_param}]"),
                            statement,
                            &format!("{params:?}"),
                        ],
                    )
                    .await?;
                }
                false => (),
            },
        };
        Ok(())
    }

    /// Update the last modified times of each of the given tables in the table cache.
    async fn update_last_modified_times(&self, tables: &[&str]) -> Result<(), DbError> {
        match self.get_caching_strategy() {
            CachingStrategy::Memory(_) => {
                let mut cache = get_memory_table_cache()?;
                let epoch_now = SystemTime::now()
                    .duration_since(UNIX_EPOCH)
                    .map_err(|err| {
                        DbError::DataError(format!("Error getting epoch time: {err}"))
                    })?;
                for table in tables {
                    cache.insert(table.to_string(), epoch_now.as_millis());
                }
            }
            _ => {
                if self.table_exists(TABLE_CACHE_TABLE).await? {
                    for table in tables {
                        let sql = format!(
                            r#"INSERT INTO "{TABLE_CACHE_TABLE}" ("table", "last_modified")
                               VALUES ({prefix}1, {ts})
                               ON CONFLICT ("table") DO UPDATE SET "last_modified" = {ts}"#,
                            prefix = self.kind().param_prefix(),
                            ts = self.kind().get_epoch_time_sql(),
                        );
                        self.execute_no_cache(&sql, params![table]).await?;
                    }
                }
            }
        };
        Ok(())
    }

    /// Delete the entries for the tables in the given list (independently of the current
    /// caching strategy) from the cache table, if it exists. If the given list is empty,
    /// clear the entire cache.
    async fn delete_query_cache_entries(&self, tables: &[&str]) -> Result<(), DbError> {
        if self.table_exists(QUERY_CACHE_TABLE).await? {
            if tables.is_empty() {
                self.execute(&format!(r#"DELETE FROM "{QUERY_CACHE_TABLE}""#), ())
                    .await?;
            } else {
                for table in tables {
                    let table_param = format!(r#"%"{table}"%"#);
                    self.execute(
                        &format!(
                            r#"DELETE FROM "{QUERY_CACHE_TABLE}" WHERE "tables" LIKE {}1"#,
                            self.kind().param_prefix()
                        ),
                        &[table_param],
                    )
                    .await?;
                }
            }
        }
        Ok(())
    }

    /// Get the SQL code that is used to define the given view.
    async fn get_view_sql(&self, view: &str) -> Result<String, DbError> {
        let view_sql = {
            let rows: Vec<DbRow> = {
                let (sql, params) = self.kind().view_sql_sql(view);
                self.query_no_cache(&sql, &params).await?
            };
            match rows.first() {
                Some(row) => row
                    .get("sql")
                    .ok_or(DbError::DataError("No column 'sql' in row".to_string()))?
                    .to_string(),
                None => {
                    return Err(DbError::DataError(format!(
                        "No view definition found for '{view}'"
                    )));
                }
            }
        };
        Ok(view_sql)
    }

    /// Uses the current caching strategy to clear the query cache for any of the given tables
    /// that (a) are views and (b) have source tables that have been modified more recently than
    /// the view. This function works both with database and memory cache strategies.
    async fn update_cached_views(&self, tables: &[&str]) -> Result<(), DbError> {
        let views = self.which_are_views(tables).await?;
        match self.get_caching_strategy() {
            CachingStrategy::Memory(_) => {
                for view in &views {
                    let last_verified = {
                        let mut last_verified = 0;
                        for (key, value) in get_memory_query_cache()?.iter() {
                            if key.tables.contains(view) && value.last_verified > last_verified {
                                last_verified = value.last_verified;
                            }
                        }
                        last_verified
                    };
                    let view_sql = self.get_view_sql(&view).await?;
                    let view_tables = get_view_tables(&view_sql)?;
                    let last_modified = {
                        let mut latest_last_modified = 0;
                        for view_table in &view_tables {
                            match get_memory_table_cache()?.get(view_table) {
                                Some(lm) if *lm > latest_last_modified => {
                                    latest_last_modified = *lm;
                                }
                                _ => (),
                            };
                        }
                        latest_last_modified
                    };
                    if last_modified >= last_verified {
                        clear_memory_query_cache(&[view])?;
                    }
                }
            }
            _ => {
                for view in &views {
                    let last_verified = self.last_verified(&view).await?;
                    let view_sql = self.get_view_sql(&view).await?;
                    let view_tables = get_view_tables(&view_sql)?;
                    let last_modified = self
                        .get_latest_last_modified(
                            &view_tables.iter().map(|t| t.as_str()).collect::<Vec<_>>(),
                        )
                        .await?;
                    if last_modified >= last_verified {
                        self.delete_query_cache_entries(&[&view]).await?;
                    }
                }
            }
        };
        Ok(())
    }

    /// Returns the latest of the last modified times of the given tables in the table cache.
    async fn get_latest_last_modified(&self, tables: &[&str]) -> Result<u64, DbError> {
        match self.table_exists(TABLE_CACHE_TABLE).await? {
            true => {
                let prefix = self.kind().param_prefix().to_string();
                let mut placeholders = vec![];
                let mut parameters = vec![];
                for (i, table) in tables.iter().enumerate() {
                    let i = i + 1;
                    placeholders.push(format!("{prefix}{i}"));
                    parameters.push(ParamValue::from(*table));
                }
                let placeholders = placeholders.join(",");

                let sql = format!(
                    r#"SELECT "last_modified"
                       FROM "{TABLE_CACHE_TABLE}"
                       WHERE "table" IN ({placeholders})
                       ORDER BY "last_modified" DESC
                       LIMIT 1"#,
                );
                let rows: Vec<DbRow> = self.query_no_cache(&sql, parameters).await?;
                match rows.len() {
                    0 => Ok(0),
                    1 => {
                        let row = &rows[0];
                        match row.get("last_modified") {
                            Some(last_modified) => Ok(last_modified.try_into()?),
                            None => Err(DbError::DataError(format!(
                                "No field 'last_modified' in row {row:?}"
                            ))),
                        }
                    }
                    too_many => Err(DbError::DataError(format!(
                        "Too many rows returned: {too_many} from table {TABLE_CACHE_TABLE}"
                    ))),
                }
            }
            false => Ok(0),
        }
    }

    /// Gets the last time the given table was modified, as read from the table cache table.
    /// If there is no entry for the table in the table cache, or if the table cache does not
    /// exist, returns 0.
    async fn last_modified(&self, table: &str) -> Result<u64, DbError> {
        self.get_latest_last_modified(&[table]).await
    }

    /// Gets the last time that the given table was verified, as read from the query cache table.
    /// If there is no entry involving the given table in the query cache, or if the query cache
    /// table doesn't exist, returns 0.
    async fn last_verified(&self, table: &str) -> Result<u64, DbError> {
        match self.table_exists(QUERY_CACHE_TABLE).await? {
            true => {
                let sql = format!(
                    r#"SELECT MAX("last_verified") AS "last_verified"
                       FROM "{QUERY_CACHE_TABLE}"
                       WHERE "tables" LIKE {p}1"#,
                    p = self.kind().param_prefix(),
                );
                let table_param = format!(r#"%"{table}"%"#);
                let rows: Vec<DbRow> = self.query_no_cache(&sql, &[&table_param]).await?;
                match rows.first() {
                    Some(row) => match row.get("last_verified") {
                        Some(value) if *value == ParamValue::Null => Ok(0),
                        Some(value) => Ok(value.try_into()?),
                        None => Err(DbError::DataError(format!(
                            "No 'last_verified' found in row: {row:?}"
                        ))),
                    },
                    None => Ok(0),
                }
            }
            false => Ok(0),
        }
    }

    /// Execute a SQL command, returning a vector of rows. If the result of the command exists
    /// in the cache, get the value from it instead of from the table(s) actually mentioned in the
    /// command, using the given [CachingStrategy].
    async fn cache<T: FromDbRows>(
        &self,
        tables: &[&str],
        sql: &str,
        params: impl IntoParams + Send + Copy + Sync,
    ) -> Result<T, DbError> {
        let db_cache =
            async |tables: &[&str], sql: &str, params: &Params| -> Result<Vec<DbRow>, DbError> {
                // Look in the cache to see if there is an entry corresponding to the given SQL
                // string for the given tables and parameters. If so, return the data from the
                // cache, otherwise execute the given SQL statement on the actualy specified
                // tables.
                let prefix = self.kind().param_prefix().to_string();
                let cache_sql = format!(
                    r#"SELECT {prefix}1||rtrim(ltrim("value", '['), ']')||{prefix}2 AS "value"
                       FROM "{QUERY_CACHE_TABLE}"
                       WHERE "tables" = {prefix}3
                       AND "statement" = {prefix}4
                       AND "parameters" = {prefix}5
                       LIMIT 1"#,
                );
                let tables_param = format!(
                    "[{}]",
                    tables
                        .iter()
                        .map(|table| format!("\"{table}\""))
                        .collect::<Vec<_>>()
                        .join(", ")
                );
                let params_param = match params {
                    Params::None => "[]".to_string(),
                    Params::Positional(params) => {
                        let params = params.iter().map(|p| p.into()).collect::<Vec<String>>();
                        format!("[{}]", params.join(", "))
                    }
                };
                let cache_params = &["[", "]", &tables_param, sql, &params_param];

                let strings = {
                    let rows: Vec<DbRow> = self.query_no_cache(&cache_sql, cache_params).await?;
                    let strings = rows
                        .iter()
                        .map(|row| match row.values().nth(0) {
                            Some(value) => Ok(value.into()),
                            None => Err(DbError::DataError("Empty row".to_owned())),
                        })
                        .collect::<Vec<_>>();
                    let strings: Result<Vec<String>, DbError> = strings.into_iter().collect();
                    strings?
                };
                match strings.first() {
                    Some(values) => {
                        let json_rows: Vec<JsonRow> = match serde_json::from_str(&values) {
                            Ok(json_rows) => json_rows,
                            _ => {
                                return Err(DbError::DataError(format!(
                                    "Invalid cache values: {values}"
                                )));
                            }
                        };
                        let db_rows = json_rows.into_db_rows();
                        // Only views need to be verified every time they are accessed. Tables
                        // do not because they do not have any dependencies.
                        if self.which_are_views(tables).await?.len() > 0 {
                            self.update_last_verified(tables, sql, &params).await?;
                        }
                        Ok(db_rows)
                    }
                    None => {
                        let db_rows: Vec<DbRow> = self.query_no_cache(sql, params).await?;
                        let json_rows: Vec<JsonRow> = FromDbRows::from(db_rows.clone());
                        let json_rows_content = json!(json_rows).to_string();
                        let insert_sql = format!(
                            r#"INSERT INTO "{QUERY_CACHE_TABLE}"
                               ("tables", "statement", "parameters", "value")
                               VALUES ({prefix}1, {prefix}2, {prefix}3, {prefix}4)"#,
                            prefix = self.kind().param_prefix(),
                        );
                        let insert_params = [&tables_param, sql, &params_param, &json_rows_content];
                        self.execute_no_cache(&insert_sql, &insert_params).await?;
                        Ok(db_rows)
                    }
                }
            };

        let mem_cache = async |tables: &[&str],
                               sql: &str,
                               params: &Params,
                               cache_size: usize|
               -> Result<Vec<DbRow>, DbError> {
            let params = &params.into_params();
            let mem_key = MemoryQueryCacheKey {
                tables: format!(
                    "[{}]",
                    tables
                        .iter()
                        .map(|table| format!("\"{table}\""))
                        .collect::<Vec<_>>()
                        .join(", ")
                ),
                statement: sql.to_string(),
                parameters: format!("{params:?}"),
            };
            let cached_rows = {
                let cache = get_memory_query_cache()?;
                match cache.get(&mem_key) {
                    Some(mem_value) => Some(mem_value.content.to_vec()),
                    None => None,
                }
            };
            match cached_rows {
                Some(json_rows) => {
                    // Only views need to be verified every time they are accessed. Tables
                    // do not because they do not have any dependencies.
                    if self.which_are_views(tables).await?.len() > 0 {
                        self.update_last_verified(tables, sql, &params).await?;
                    }
                    let db_rows = json_rows.into_db_rows();
                    Ok(db_rows)
                }
                None => {
                    let db_rows: Vec<DbRow> = self.query_no_cache(sql, params).await?;
                    let json_rows: Vec<JsonRow> = FromDbRows::from(db_rows.clone());
                    let mut cache = get_memory_query_cache()?;
                    // If the number of entries exceeds the allowed cache size, remove the oldest
                    // keys first.
                    // TODO: We may want to do something smarter here. E.g., we could record the
                    // length of time a query takes and/or the number of times it was requested
                    // in order to determine which entries to delete.
                    while cache.len() > cache_size {
                        cache.shift_remove_index(0);
                    }
                    cache.insert(
                        mem_key,
                        MemoryQueryCacheValue {
                            content: json_rows.to_vec(),
                            last_verified: SystemTime::now()
                                .duration_since(UNIX_EPOCH)
                                .map_err(|err| {
                                    DbError::DataError(format!("Error getting epoch time: {err}"))
                                })?
                                .as_millis(),
                        },
                    );
                    Ok(db_rows)
                }
            }
        };

        // Now proceed to lookup the given query for the given tables in the cache:
        match self.get_caching_strategy() {
            CachingStrategy::None => {
                let rows: Vec<DbRow> = self.query_no_cache(sql, params).await?;
                Ok(FromDbRows::from(rows))
            }
            CachingStrategy::TruncateAll | CachingStrategy::Truncate => {
                self.ensure_cache_tables_exist().await?;
                self.update_cached_views(tables).await?;
                let rows: Vec<DbRow> = db_cache(tables, sql, &params.into_params()).await?;
                Ok(FromDbRows::from(rows))
            }
            CachingStrategy::Trigger => {
                let views = self
                    .which_are_views(tables)
                    .await?
                    .into_iter()
                    .collect::<HashSet<_>>();
                for table in tables
                    .into_iter()
                    .cloned()
                    .collect::<HashSet<_>>()
                    .difference(&views.iter().map(|v| v.as_str()).collect::<HashSet<_>>())
                    .collect::<Vec<_>>()
                {
                    self.ensure_caching_triggers_exist_for_table(&table).await?;
                }
                for view in &views {
                    self.ensure_caching_triggers_exist_for_view(view).await?;
                }
                let rows: Vec<DbRow> = db_cache(&tables, sql, &params.into_params()).await?;
                Ok(FromDbRows::from(rows))
            }
            CachingStrategy::Memory(cache_size) => {
                self.update_cached_views(tables).await?;
                let rows = mem_cache(tables, sql, &params.into_params(), cache_size).await?;
                Ok(FromDbRows::from(rows))
            }
        }
    }

    /// Given a table, return a map from column names to column SQL types.
    async fn columns(&self, table: &str) -> Result<ColumnMap, DbError> {
        let mut columns = ColumnMap::new();
        let (sql, params) = self.kind().columns_sql(table);
        let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
        for row in &rows {
            match (
                row.get("column_name")
                    .and_then(|value| Some::<String>(value.into())),
                row.get("data_type")
                    .and_then(|value| Some::<String>(value.into())),
            ) {
                (Some(column), Some(data_type)) => {
                    columns.insert(column.to_string(), data_type.to_lowercase().to_string())
                }
                _ => {
                    return Err(DbError::DataError(format!(
                        "Error getting columns for table '{table}'"
                    )));
                }
            };
        }

        match columns.is_empty() {
            true => Err(DbError::DataError(format!(
                "No information found for table '{table}'"
            ))),
            false => Ok(columns),
        }
    }

    /// Retrieve the primary key columns for a given table.
    async fn primary_keys(&self, table: &str) -> Result<Vec<String>, DbError> {
        let (sql, params) = self.kind().primary_keys_sql(table);
        let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
        rows.iter()
            .map(|row| {
                match row
                    .get("column_name")
                    .and_then(|name| Some::<String>(name.into()))
                {
                    Some(pk_col) => Ok(pk_col.to_string()),
                    None => Err(DbError::DataError("Empty row".to_owned())),
                }
            })
            .collect()
    }

    /// Execute a SQL command, returning nothing.
    async fn execute(&self, sql: &str, params: impl IntoParams + Send) -> Result<(), DbError> {
        let params = params.into_params();
        let _: Vec<DbRow> = match params {
            Params::None => self.query(sql, ()).await?,
            _ => self.query(sql, params).await?,
        };
        Ok(())
    }

    /// Execute a SQL command, returning nothing, without updating the cache, regardless of
    /// whether cache_aware_query option (see [DbQuery::set_cache_aware_query()]) has been set.
    async fn execute_no_cache(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<(), DbError> {
        let params = params.into_params();
        let _: Vec<DbRow> = match params {
            Params::None => self.query_no_cache(sql, ()).await?,
            _ => self.query_no_cache(sql, params).await?,
        };
        Ok(())
    }

    /// Sequentially execute a semicolon-delimited list of statements, without parameters.
    fn execute_batch(&self, sql: &str) -> impl Future<Output = Result<(), DbError>> + Send;

    /// Execute a SQL command, returning a vector of rows.
    async fn query<T: FromDbRows + Send>(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<T, DbError> {
        let rows = self.query_no_cache(sql, params).await?;
        if self.get_cache_aware_query() {
            self.clear_cache_for_affected_tables(sql).await?;
        }
        Ok(rows)
    }

    /// Execute a SQL command, returning a vector of rows, without updating the cache,
    /// regardless of whether the cache_aware_query option (see [DbQuery::set_cache_aware_query()])
    /// has been set.
    fn query_no_cache<T: FromDbRows>(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> impl Future<Output = Result<T, DbError>> + Send;

    /// Execute a SQL command, returning a single row.
    async fn query_row(&self, sql: &str, params: impl IntoParams + Send) -> Result<DbRow, DbError> {
        let rows: Vec<DbRow> = self.query(&sql, params).await?;
        if rows.len() > 1 {
            return Err(DbError::DataError(
                "More than one row returned for query_row()".to_string(),
            ));
        }
        match rows.into_iter().next() {
            Some(row) => Ok(row),
            None => Err(DbError::DataError("No row found".to_string())),
        }
    }

    /// Execute a SQL command, returning a single value.
    async fn query_value(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<ParamValue, DbError> {
        let row = self.query_row(sql, params).await?;
        if row.len() > 1 {
            return Err(DbError::DataError(
                "More than one value returned for query_value()".to_string(),
            ));
        }
        match row.values().next() {
            Some(value) => Ok(value.clone()),
            None => Err(DbError::DataError("No values found".to_string())),
        }
    }

    /// Execute a SQL command, returning a single string.
    async fn query_string(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<String, DbError> {
        let value = self.query_value(sql, params).await?;
        Ok(value.into())
    }

    /// Execute a SQL command, returning a vector of strings: the first value for each row.
    async fn query_strings(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<Vec<String>, DbError> {
        let rows: Vec<DbRow> = self.query(sql, params).await?;
        rows.iter()
            .map(|row| match row.values().nth(0) {
                Some(value) => Ok(value.into()),
                None => Err(DbError::DataError("Empty row".to_owned())),
            })
            .collect()
    }

    /// Execute a SQL command, returning a row of strings.
    async fn query_string_row(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<StringRow, DbError> {
        let row = self.query_row(sql, params).await?;
        Ok(row
            .iter()
            .map(|(key, value)| (key.clone(), value.into()))
            .collect())
    }

    /// Execute a SQL command, returning a vector of rows of strings.
    async fn query_string_rows(
        &self,
        sql: &str,
        params: impl IntoParams + Send,
    ) -> Result<Vec<StringRow>, DbError> {
        let rows: Vec<DbRow> = self.query(sql, params).await?;
        Ok(db_rows_to_string_rows(&rows))
    }

    /// Execute a SQL command, returning a single unsigned integer.
    async fn query_u64(&self, sql: &str, params: impl IntoParams + Send) -> Result<u64, DbError> {
        let value = self.query_value(sql, params).await?;
        Ok(value.try_into()?)
    }

    /// Execute a SQL command, returning a single signed integer.
    async fn query_i64(&self, sql: &str, params: impl IntoParams + Send) -> Result<i64, DbError> {
        let value = self.query_value(sql, params).await?;
        Ok(value.try_into()?)
    }

    /// Execute a SQL command, returning a single float.
    async fn query_f64(&self, sql: &str, params: impl IntoParams + Send) -> Result<f64, DbError> {
        let value = self.query_value(sql, params).await?;
        Ok(value.try_into()?)
    }

    /// Insert rows into the given table. If an input row does not have a key for a column,
    /// use NULL as the value of that column when inserting the row to the table.
    fn insert(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::insert()], but in addition this function also returns the columns from the
    /// inserted data that are included in `returning`, or all of the inserted data if `returning`
    /// is an empty list.
    fn insert_returning<T: FromDbRows>(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<T, DbError>>;

    /// Update the given table using the given rows. The table should have a primary key
    /// and any columns included in the primary key should be present within each input row.
    /// The primary key column values will be used as a way of identifying the rows to update,
    /// while the other columns in the row will be updated to the given new values.
    fn update(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::update()], but in addition this function also returns the columns from the
    /// updated data that are included in `returning`, or all of the updated data if `returning`
    /// is an empty list.
    fn update_returning<T: FromDbRows>(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<T, DbError>>;

    /// Attempt to insert the given rows to the given table, similarly to [DbQuery::insert()].
    /// In case there is a conflict, update the table instead, similarly to [DbQuery::update()].
    fn upsert(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
    ) -> impl Future<Output = Result<(), DbError>>;

    /// Like [DbQuery::upsert()], but in addition this function also returns the columns from the
    /// upserted data that are included in `returning`, or all of the upserted data if `returning`
    /// is an empty list.
    fn upsert_returning<T: FromDbRows>(
        &self,
        table: &str,
        columns: &[&str],
        rows: impl IntoDbRows,
        returning: &[&str],
    ) -> impl Future<Output = Result<T, DbError>>;

    /// Check whether the given table exists in the database.
    async fn table_exists(&self, table: &str) -> Result<bool, DbError> {
        Ok(self.which_are_tables(&[table]).await?.len() == 1)
    }

    /// Check whether the given view exists in the database.
    async fn view_exists(&self, view: &str) -> Result<bool, DbError> {
        Ok(self.which_are_views(&[view]).await?.len() == 1)
    }

    /// Return a list of the objects from the given list that are tables.
    async fn which_are_tables(&self, objects: &[&str]) -> Result<Vec<String>, DbError> {
        let mut tables = vec![];
        let mut unknowns = vec![];
        for object in objects {
            if exists_in_meta_cache(&format!("{object}_TABLE"))? {
                tables.push(object.to_string());
            } else if !exists_in_meta_cache(&format!("{object}_VIEW"))? {
                unknowns.push(object.to_string());
            }
        }

        // Query the database for the status of any unknowns:
        if !unknowns.is_empty() {
            let (sql, params) = self
                .kind()
                .which_are_tables_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
            let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
            for row in &rows {
                let table = row
                    .get("table_name")
                    .ok_or(DbError::DataError("No table_name found in row".to_string()))?
                    .to_string();
                let mut cache = get_meta_cache()?;
                cache.insert(format!("{table}_TABLE"));
                tables.push(table);
            }

            let (sql, params) = self
                .kind()
                .which_are_views_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
            let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
            for row in &rows {
                let view = row
                    .get("view_name")
                    .ok_or(DbError::DataError("No view_name found in row".to_string()))?
                    .to_string();
                let mut cache = get_meta_cache()?;
                cache.insert(format!("{view}_VIEW"));
            }
        }
        Ok(tables)
    }

    /// Return a list of the objects from the given list that are views.
    async fn which_are_views(&self, objects: &[&str]) -> Result<Vec<String>, DbError> {
        let mut views = vec![];
        let mut unknowns = vec![];
        for object in objects {
            if exists_in_meta_cache(&format!("{object}_VIEW"))? {
                views.push(object.to_string());
            } else if !exists_in_meta_cache(&format!("{object}_TABLE"))? {
                unknowns.push(object.to_string());
            }
        }

        // Query the database for the status of any unknowns:
        if !unknowns.is_empty() {
            let (sql, params) = self
                .kind()
                .which_are_views_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
            let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
            for row in &rows {
                let view = row
                    .get("view_name")
                    .ok_or(DbError::DataError("No view_name found in row".to_string()))?
                    .to_string();
                let mut cache = get_meta_cache()?;
                cache.insert(format!("{view}_VIEW"));
                views.push(view);
            }

            let (sql, params) = self
                .kind()
                .which_are_tables_sql(&unknowns.iter().map(|s| s.as_str()).collect::<Vec<_>>());
            let rows: Vec<DbRow> = self.query_no_cache(&sql, params).await?;
            for row in &rows {
                let table = row
                    .get("table_name")
                    .ok_or(DbError::DataError("No table_name found in row".to_string()))?
                    .to_string();
                let mut cache = get_meta_cache()?;
                cache.insert(format!("{table}_TABLE"));
            }
        }
        Ok(views)
    }

    /// Drop the given table from the database. Note that for PostgreSQL (see
    /// <https://www.postgresql.org/docs/current/sql-droptable.html>), in the case of a
    /// dependent foreign key constraint, only the constraint will be removed, not the dependent
    /// table itself.
    async fn drop_table(&self, table: &str) -> Result<(), DbError> {
        let table = validate_table_name(table)?;
        // Drop the table:
        match self.kind() {
            DbKind::PostgreSQL => {
                self.execute_no_cache(&format!(r#"DROP TABLE IF EXISTS "{table}" CASCADE"#), ())
                    .await?
            }
            DbKind::SQLite => {
                self.execute_no_cache(&format!(r#"DROP TABLE IF EXISTS "{table}""#), ())
                    .await?
            }
        };

        // Delete dirty entries from the cache in accordance with our caching strategy:
        self.clear_cache_for_dropped_tables(&[&table]).await?;
        Ok(())
    }

    /// Drop the given view from the database.
    async fn drop_view(&self, view: &str) -> Result<(), DbError> {
        let view = validate_table_name(view)?;
        // Drop the view:
        match self.kind() {
            DbKind::PostgreSQL => {
                self.execute_no_cache(&format!(r#"DROP VIEW IF EXISTS "{view}" CASCADE"#), ())
                    .await?
            }
            DbKind::SQLite => {
                self.execute_no_cache(&format!(r#"DROP VIEW IF EXISTS "{view}""#), ())
                    .await?
            }
        };

        // Delete dirty entries from the cache in accordance with our caching strategy:
        self.clear_cache_for_dropped_tables(&[&view]).await?;
        Ok(())
    }
}

